<strong>ALCANCE DE LA CONSULTORÍA</p></strong>
Como parte del proyecto La consultoría tiene un enfoque provincial dirigido a la academia y a la
industria para determinar los sectores productivos a ser priorizados para el proyecto y definir
oportunidades de creación de empresas en base a las necesidades de mercado determinadas. La
consultoría planificará, levantará información, producirá un estudio cuantitativo y cualitativo para
su posterior análisis y sistematización de las necesidades del sector académico e industrial según
los objetivos del proyecto.</p>
El diagnóstico será realizado a los siguientes stakeholders de la provincia:</p>
• I.S.T. “Guayaquil”<br>
• Cámara de Industrias<br>
• Cámara de Comercio<br>
• Institutos Tecnológicos<br>
• Universidades</p>
El diagnóstico situacional consiste en:</p>
• Selección de beneficiarios de la academia y sectores priorizados de la industria<br>
• Análisis de la situación de los jóvenes estudiantes y la academia<br>
• Análisis de la situación empresas de los sectores priorizados<br>
• Determinación de necesidades de los beneficiarios y las empresas<br>
• Sistematización de insumos para el desarrollo de la metodología de formación de
capacidades emprendedoras.</p>
